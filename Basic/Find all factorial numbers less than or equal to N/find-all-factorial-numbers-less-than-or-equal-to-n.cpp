//User function Template for C++
class Solution
{
public:
    vector<long long> factorialNumbers(long long N)
    {
        vector<long long>ans; unsigned long long j=1; unsigned long long fact = 1;
        // Write Your Code here
        while(fact<=N)
        {
            ans.push_back(fact);
            j++;
            if(fact >N/j)
            {
                break;
            }
            fact*=j;
        }
        return ans;
    }
};

